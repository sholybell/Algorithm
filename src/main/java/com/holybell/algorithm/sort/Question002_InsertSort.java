    /**
     * 对给定数组进行插入排序
     *
     * @param a 给定数组
     */
    private static void selectSort(int[] a) {

        if (a == null || a.length == 0) {
            return;
        }

        for (int i = 1; i < a.length; i++) {    // i位置前已经排好序
            for (int j = i; j > 0; j--) {       // j位置开始排序，往前一个个比较插入相应位置，该位置和后续元素后移
                if (a[j - 1] > a[j]) {
                    swap(a, j, j - 1);
                } else {                        // 已经比较到一个比当前元素小的元素，再往前更小，无须继续比较
                    break;
                }
            }
        }
    }

    public static void main(String[] args) {
        int array[] = {5, 4, 9, 8, 7, 6, 0, 1, 3, 2};
        myInsertSort(array);
        System.out.println(Arrays.toString(array));
    }
