    public static void BubbleSort(int a[]) {

        if (a == null || a.length == 0) {
            return;
        }

        for (int i = 0; i < a.length; i++) {    // i表示本次要排序的位置，i循环完毕会将数组第i小的元素放到i的位置
            for (int j = a.length - 1; j > i; j--) {    // 从最后一个元素开始，两两比较，前者大的互相交换，直到比较到i的位置
                if (a[j - 1] > a[j]) {
                    swap(a, j - 1, j);
                }
            }
        }
    }

    public static void main(String[] args) {
        int a[] = {5, 4, 9, 8, 7, 6, 0, 1, 3, 2};
        myBubbleSort(a);
        System.out.println(Arrays.toString(a));
    }
