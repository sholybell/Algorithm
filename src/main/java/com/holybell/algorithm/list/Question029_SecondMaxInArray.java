    /**
     * 从给定数组中找到第二大的数字
     *
     * @param a 给定数组
     */
    private static void findSecMaxValue(int[] a) {

        // 空数组不处理
        if (a == null || a.length == 0) {
            return;
        }

        int _1Max = a[0];
        int _2Max = a[0];

        for (int num : a) {
            // 每次发现一个更大的数字赋值给最大值，将原先最大值赋值给第二大值
            if (num > _1Max) {
                _2Max = _1Max;
                _1Max = num;
            }
        }
        System.out.println("第二大的数字为:" + _2Max);
    }


    public static void main(String[] args) {
        int a[] = {7, 3, 19, 40, 4, 7, 1, 100};
        findSecMaxValue(a);
        myFindSecMaxValue(a);
    }
