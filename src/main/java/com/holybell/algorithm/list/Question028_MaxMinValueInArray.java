    /**
     * 求给定数组的最大值和最小值
     *
     * @param a 给定数组
     */
    private static void getMaxAndMin(int[] a) {

        // 给定数组为空不处理
        if (a == null || a.length == 0) {
            return;
        }

        int max = Integer.MIN_VALUE;
        int min = Integer.MAX_VALUE;

        for (int anA : a) {
            if (anA > max) {
                max = anA;
            }
            if (anA < min) {
                min = anA;
            }
        }
        System.out.println("最大值:" + max);
        System.out.println("最小值:" + min);
    }


